<template>
  <transition name="fade">
    <div
      v-if="showing"
      class="fixed bottom-0 inset-x-0 px-4 pb-4 sm:inset-0 sm:flex sm:items-center sm:justify-center z-50"
      @click.self="close"
    >
      <!-- modal background -->
      <div class="fixed inset-0" @click="close()">
        <div class="absolute inset-0 bg-black opacity-75" />
      </div>

      <!-- modal content -->
      <div class="sm:w-full sm:max-w-xl bg-white rounded-lg overflow-hidden shadow-xl transform transition-all">
        <slot />
      </div>
    </div>
  </transition>
</template>

<script>
export default {
  props: {
    showing: {
      type: Boolean,
      description: 'Whether the modal is shown',
      default: false
    }
  },
  watch: {
    showing (value) {
      value
        ? document.querySelector('body').classList.add('overflow-hidden')
        : document.querySelector('body').classList.remove('overflow-hidden')
    }
  },
  mounted () {
    document.addEventListener('keydown', (e) => {
      if (e.keyCode === 27) { this.close() }
    })
  },
  methods: {
    close () {
      this.$emit('close')
    }
  }
}
</script>

<style>
.fade-enter-active,
.fade-leave-active {
  transition: all 0.3s;
}
.fade-enter,
.fade-leave-to {
  opacity: 0;
  margin-top: -75px;
}
</style>
